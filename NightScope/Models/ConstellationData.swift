/// 星座線の両端点 (J2000.0 赤道座標)
struct ConstellationSegment {
    let ra1: Double   // 赤経 (度)
    let dec1: Double  // 赤緯 (度)
    let ra2: Double
    let dec2: Double
}

/// 星座エントリ (日本語名 + 中心座標 + 星座線セグメント群)
